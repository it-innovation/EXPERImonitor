package uk.co.soton.itinnovation.ecc.service.domain;

import java.util.Date;
import java.util.UUID;

/**
 *
 */
public class EccExperiment {

    private UUID uuid;
    private String name, description, phase, status, projectName;
    private Date startTime, endTime;

    public EccExperiment() {
    }

    public EccExperiment(String projectName, UUID uuid, String name, String description, String phase, String status, Date startTime, Date endTime) {
        this.projectName = projectName;
        this.uuid = uuid;
        this.name = name;
        this.description = description;
        this.phase = phase;
        this.status = status;
        this.startTime = startTime;
        this.endTime = endTime;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public UUID getUuid() {
        return uuid;
    }

    public void setUuid(UUID uuid) {
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPhase() {
        return phase;
    }

    public void setPhase(String phase) {
        this.phase = phase;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

}
